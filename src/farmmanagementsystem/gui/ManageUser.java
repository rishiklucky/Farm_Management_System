    private void loadTable() {
        list = DBQuery.getUsers();
        DefaultTableModel tbl = (DefaultTableModel) jTableUser.getModel();
        tbl.setRowCount(0);

        for (int i = 0; i < list.size(); i++) {
            tbl.addRow(list.get(i).toArray());
        }
    }

