    private void jButtonUpdateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateUserActionPerformed
        if (jTableUser.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "NO SELECTED ITEM", "EMPTY SELECTION", JOptionPane.ERROR_MESSAGE);
            return;
        }
        User selected = list.get(jTableUser.getSelectedRow());
        UpdateUser us = new UpdateUser(mainMenu, true);
        us.setUser(selected);
        us.setUser_ID(user_ID);
        us.setVisible(true);
        loadTable();
    }//GEN-LAST:event_jButtonUpdateUserActionPerformed

