    private void loadTable(){
                
        DefaultTableModel tbl = (DefaultTableModel) jTable1.getModel();  
        tbl.setRowCount(0);
        for(int i=0; i<list.size();i++){
            Log tmp = list.get(i);
            String date = String.format( "%1$tb %1$te, %1$TY\n",tmp.getLog_date());
            String time = String.format( "%tr\n", tmp.getLog_date() );
            String time1 = time.substring(0, 5),time2 = time.substring(8, 11);
            //tbl.addRow(new Object[]{tmp.getLog_id(),date,time1+time2,tmp.getUser().getUser_id(),tmp.getUser().getName(),tmp.getActivity()});
            tbl.addRow(new Object[]{date,time1+time2,tmp.getUser().getUser_id(),tmp.getUser().getName(),tmp.getActivity()});
        }
    }

    
