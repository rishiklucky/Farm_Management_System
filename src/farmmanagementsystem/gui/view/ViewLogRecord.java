    private void jButtonSEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSEARCHActionPerformed
         from = dateChooserComboFROM.getSelectedDate().getTime();
         to  = dateChooserComboTO.getSelectedDate().getTime();
        if(from.after(to)){
            JOptionPane.showMessageDialog(null, "INVALID RANGE OF DATE", "INVALID DATE", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            list = DBQuery.getLog(jComboBoxCategory.getSelectedIndex(), jTextFieldVal.getText(), from, to);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ViewLogRecord.class.getName()).log(Level.SEVERE, null, ex);
        }
        Condition = "LOG that contains ['"+jTextFieldVal.getText()+"'] on user's "+jComboBoxCategory.getSelectedItem();
        loadTable();
    }//GEN-LAST:event_jButtonSEARCHActionPerformed

