    private void jButtonSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSAVEActionPerformed
        if(!isValidFields()){
            if(!doubleMSG)
            JOptionPane.showMessageDialog(null, "INVALID DATA FIELDS", "INCOMPLETE DATA", JOptionPane.ERROR_MESSAGE);
            doubleMSG = false;
            return;
        }
        Location loc = new Location();
        loc.setAddress(jTextFieldAddress.getText());
        loc.setCareTaker((Employee) jComboBoxEmployee.getSelectedItem());
        loc.setDescription(jTextFieldDesc.getText());
        loc.setLoc_id(location.getLoc_id());
        loc.setMaintenance_share(Double.parseDouble(jFormattedTextFieldShare.getText()));
        if(loc.equals(location)){
            JOptionPane.showMessageDialog(null, "NO CHANGES", "UPDATE ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(DBQuery.isExistLocation(loc.getAddress())&& !loc.getAddress().equalsIgnoreCase(location.getAddress())){
            JOptionPane.showMessageDialog(null, "Loction already Exist", "DUPLICATION ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(confirm("Update Location?")){
            DBQuery.updateLocation(loc);
            JOptionPane.showMessageDialog(null, "SAVED", "SAVED", JOptionPane.INFORMATION_MESSAGE);
            DBQuery.insertLog(user.getUser_id(), "Update Location [Location ID:"+loc.getLoc_id()+"]");
            dispose();
        }
    }//GEN-LAST:event_jButtonSAVEActionPerformed

    private void jFormattedTextFieldShareFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextFieldShareFocusLost
        double d;
        try {
            d = Double.parseDouble(jFormattedTextFieldShare.getText());
        if(d > 40 || d < 0){
            JOptionPane.showMessageDialog(null, "Share:\n MAX:40\n MIN:0", "INVALID SHARE", JOptionPane.ERROR_MESSAGE);
            jFormattedTextFieldShare.setText("0.00");
        }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(null, "Invalid:\n Use Only\n Numeric Value", "INVALID Input", JOptionPane.ERROR_MESSAGE);
            jFormattedTextFieldShare.setText("0.00");            
        }        
    }//GEN-LAST:event_jFormattedTextFieldShareFocusLost

