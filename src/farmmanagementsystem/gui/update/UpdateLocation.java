    private void setLocation(){
        jLabelLoc_id.setText(location.getLoc_id()+"");
        jTextFieldAddress.setText(location.getAddress());
        jTextFieldDesc.setText(location.getDescription());
        jComboBoxEmployee.setSelectedItem(location.getCareTaker());
        jFormattedTextFieldShare.setText(location.getMaintenance_share()+"");
    }
private boolean doubleMSG = false;
    private boolean isValidFields(){
        if(jTextFieldAddress.getText().isEmpty() || jTextFieldDesc.getText().isEmpty() ||jFormattedTextFieldShare.getText().isEmpty())
            return false;
        double d = -1;
        try {
            d = Double.parseDouble(jFormattedTextFieldShare.getText());
        } catch (NumberFormatException numberFormatException) {
        }
        if(d > 40 || d < 0){
            doubleMSG = true;
            return false;
        }
        return true;
    }
    private boolean confirm(String confirmMessage){
        int ans = JOptionPane.showConfirmDialog(this, confirmMessage, "CONFIRMATION", JOptionPane.YES_NO_OPTION);
        return (ans == JOptionPane.YES_OPTION);
    }

