    private double gross=0,act_share,CT_share,owner_share;

    private String calculatePercentage(double share,double gross){
        return String.format( "%,.2f",(share/gross)*100);
    }

    public boolean viewOnly = false;
    private void setGrossProfit(){
        jLabelGP_ID.setText(grossProfit.getGp_id()+"");
        jLabelDate.setText(String.format( "%1$tb %1$td, %1$tY\n",grossProfit.getDate()));
        jLabelLocationID.setText(grossProfit.getLocation().getLoc_id()+"");
        jLabelLocationAddress.setText(grossProfit.getLocation().getAddress());
        jLabelMaintenancePerson.setText(grossProfit.getLocation().getCareTaker().getName());
             gross = grossProfit.getGross_amount();
             act_share = grossProfit.getActivity_share();
             CT_share = grossProfit.getMaintainer_share();
             owner_share = grossProfit.getOwner_share();
             jLabelActivityShare.setText(String.format( "%,.2f",act_share)+" ("+calculatePercentage(act_share, gross)+"%)");
             jLabelMaintenanceShare.setText(String.format( "%,.2f",CT_share)+" ("+calculatePercentage(CT_share, gross)+"%)");
             jLabelOwnerShare.setText(String.format( "%,.2f",owner_share));
             jLabelNET.setText(String.format( "%,.2f",owner_share));
        jTextFieldRemarks.setText(grossProfit.getRemarks());
        jListEmpList.setListData(DBQuery.getEmployeeOnActivity(grossProfit.getActivity().getAct_id()).toArray());
        jLabelLocationGrossAmount.setText(String.format( "%,.2f",grossProfit.getGross_amount()));
        jButtonSAVE.setEnabled(!viewOnly);
        jTextFieldRemarks.setEditable(!viewOnly);
    }

    private boolean confirm(String confirmMessage){
        int ans = JOptionPane.showConfirmDialog(this, confirmMessage, "CONFIRMATION", JOptionPane.YES_NO_OPTION);
        return (ans == JOptionPane.YES_OPTION);
    }


    private ArrayList<Expenses> expList;
    private double totalExpense = 0;
    private void loadTable(){
        expList = DBQuery.getExpensesByGP_ID(grossProfit.getGp_id());
        double total =0;
        for(int i=0; i<expList.size();i++){
            total += expList.get(i).getAmount();
        }
        totalExpense = total;
        jLabelEXPENSES.setText(String.format( "%,.2f",total));
        DefaultTableModel tbl = (DefaultTableModel) jTable1.getModel();
        tbl.setRowCount(0);
        for(Expenses e:expList){
            tbl.addRow(new Object[]{String.format("%1$ta, %1$tb %1$te, %1$tY", e.getDate()),e.getReference(),e.getAmount(),e.getRemarks()});
        }
    }
