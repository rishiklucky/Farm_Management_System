    private void jButtonSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSAVEActionPerformed
             if(jTextFieldRemarks.getText().isEmpty()){
                 JOptionPane.showMessageDialog(null, "EMPTY REMARKS!", "INVALID DATA", JOptionPane.ERROR_MESSAGE);
                 return;
             }
             if(grossProfit.getRemarks().equalsIgnoreCase(jTextFieldRemarks.getText())){
                 JOptionPane.showMessageDialog(null, "NO CHANGES MADE!", "NO CHANGES", JOptionPane.ERROR_MESSAGE);
                 return;
             }
            grossProfit.setRemarks(jTextFieldRemarks.getText());
            DBQuery.updateGrossProfitRemarks(jTextFieldRemarks.getText(), grossProfit.getGp_id());
            JOptionPane.showMessageDialog(null, "UPDATED", "UPDATES", JOptionPane.INFORMATION_MESSAGE);
            DBQuery.insertLog(user.getUser_id(), "GROSS PROFIT UPDATE [GP ID: "+grossProfit.getGp_id()+"]");
            dispose();
    }//GEN-LAST:event_jButtonSAVEActionPerformed

