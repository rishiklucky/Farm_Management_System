    private void jButtonSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSAVEActionPerformed

        if(!isValidField()){
            JOptionPane.showMessageDialog(null, "Invalid Data", "INPUT ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        GrossProfit gp = new GrossProfit();
        gp.setActivity_share(act_share);
        gp.setDate(dateChooserCombo1.getSelectedDate().getTime());
        gp.setGp_id(generatedID);
        gp.setGross_amount(gross);
        gp.setLocation((Location) jComboBoxLocation.getSelectedItem());
        gp.setMaintainer_share(CT_share);
        gp.setOwner_share(owner_share);
        gp.setRemarks(jTextFieldRemarks.getText());
        gp.setActivity(activity);
        if(confirm("SAVE NEW GROSS PROFIT ENTRY?")){
            DBQuery.addGrossProfit(gp);
            toSalary();//call for computaion of salary
            postingExpenses();
            DBQuery.insertLog(user.getUser_id(), "GROSS PROFIT ENTRY [GP ID:"+gp.getGp_id()+"]");
            dispose();
        }
    }//GEN-LAST:event_jButtonSAVEActionPerformed

