    private void toSalary(){
        ListModel li = jListEmpList.getModel();
        
        double individualShare = act_share/li.getSize();
//computation for activity share
        for(int i = 0; i<li.getSize();i++){
            Employee e = (Employee) li.getElementAt(i);
            Salary s = new Salary();
            s.setAmount(individualShare);
            s.setEmpID(e.getEmp_id());
            s.setGp_id(generatedID);
            s.setRemarks("Activity Share");
            DBQuery.addSalary(s);
        }
        //computation for maintainer
        activity.setPosted(true);
        DBQuery.updateActivity(activity);
        Employee CT = ((Location) jComboBoxLocation.getSelectedItem()).getCareTaker();
        Salary ct = new Salary();
        ct.setAmount(CT_share);
        ct.setEmpID(CT.getEmp_id());
        ct.setGp_id(generatedID);
        ct.setRemarks("Maintenance Share");
        DBQuery.addSalary(ct);
    }

    private void postingExpenses(){
        for(Expenses e:expList){
            e.setGp_id(generatedID);
            e.setPosted(true);
            DBQuery.deleteExpenses(e.getExp_id());
            DBQuery.addExpense(e);
        }
    }

    private ArrayList<Expenses> expList;
    private double totalExpense = 0;
    private void loadTable(int loc_id){
        expList = DBQuery.getExpenses(loc_id);
        double total =0;
        for(int i=0; i<expList.size();i++){
            if(expList.get(i).getGp_id() != 0){
                expList.remove(i--);
                continue;
            }
            total += expList.get(i).getAmount();
        }
        totalExpense = total;
        jLabelExpenses.setText(String.format( "%,.2f",total));
        DefaultTableModel tbl = (DefaultTableModel) jTable1.getModel();
        tbl.setRowCount(0);
        for(Expenses e:expList){
            tbl.addRow(new Object[]{String.format("%1$ta, %1$tb %1$te, %1$tY", e.getDate()),e.getReference(),e.getAmount(),e.getRemarks()});
        }
    }
